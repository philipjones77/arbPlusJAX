from __future__ import annotations

import argparse
import json
import os
from pathlib import Path
import subprocess
import sys
import time


REPO_ROOT = Path(__file__).resolve().parents[1]


def _base_env() -> dict[str, str]:
    env = os.environ.copy()
    env["PYTHONPATH"] = str(REPO_ROOT / "src") + os.pathsep + env.get("PYTHONPATH", "")
    env.setdefault("JAX_PLATFORMS", "cpu")
    return env


def _run_import_probe(module_expr: str) -> dict[str, float]:
    code = (
        "import time\n"
        "t0=time.perf_counter()\n"
        f"{module_expr}\n"
        "print(time.perf_counter()-t0)\n"
    )
    completed = subprocess.run(
        [sys.executable, "-c", code],
        cwd=REPO_ROOT,
        env=_base_env(),
        capture_output=True,
        text=True,
        check=True,
    )
    seconds = float(completed.stdout.strip().splitlines()[-1])
    return {"seconds": round(seconds, 6)}


def _run_worker(family: str, pad_to: int) -> dict[str, float]:
    env = _base_env()
    completed = subprocess.run(
        [sys.executable, str(Path(__file__).resolve()), "--worker", "--family", family, "--pad-to", str(pad_to)],
        cwd=REPO_ROOT,
        env=env,
        capture_output=True,
        text=True,
        check=True,
    )
    return json.loads(completed.stdout)


def _worker(family: str, pad_to: int) -> None:
    import jax
    import jax.numpy as jnp

    from arbplusjax import api
    from arbplusjax import double_interval as di

    if family != "arb_mat":
        raise ValueError(f"unsupported family {family!r}")
    dense = jnp.asarray(
        [
            [[3.0, 0.5], [0.5, 2.0]],
            [[4.0, -1.0], [-1.0, 3.5]],
            [[2.5, 0.25], [0.25, 2.25]],
            [[5.0, 0.75], [0.75, 4.0]],
        ],
        dtype=jnp.float64,
    )
    rhs = jnp.asarray(
        [
            [1.0, 2.0],
            [1.5, -0.5],
            [0.75, 0.25],
            [0.25, 1.25],
        ],
        dtype=jnp.float64,
    )
    prepare_name = "arb_mat_matvec_cached_prepare"
    apply_name = "arb_mat_matvec_cached_apply"

    dense_interval = di.interval(dense, dense)
    rhs_interval = di.interval(rhs, rhs)

    t0 = time.perf_counter()
    plan = api.eval_point(prepare_name, dense_interval[0], dtype="float64")
    jax.block_until_ready(plan)
    t1 = time.perf_counter()

    bound = jax.jit(
        lambda batch: jax.vmap(
            lambda x: api.eval_point(apply_name, plan, x, dtype="float64")
        )(batch)
    )
    t2 = time.perf_counter()
    out = bound(rhs_interval)
    jax.block_until_ready(out)
    t3 = time.perf_counter()

    t4 = time.perf_counter()
    out = bound(rhs_interval)
    jax.block_until_ready(out)
    t5 = time.perf_counter()

    payload = {
        "family": family,
        "pad_to": pad_to,
        "prepare_first_s": round(t1 - t0, 6),
        "compile_plus_first_cached_apply_s": round(t3 - t2, 6),
        "steady_cached_apply_s": round(t5 - t4, 6),
    }
    print(json.dumps(payload, indent=2))


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--worker", action="store_true")
    parser.add_argument("--family", choices=("arb_mat",), default="arb_mat")
    parser.add_argument("--pad-to", type=int, default=4)
    parser.add_argument("--out-json", default="benchmarks/results/matrix_point_startup_probe/matrix_point_startup_probe.json")
    parser.add_argument("--out-md", default="benchmarks/results/matrix_point_startup_probe/matrix_point_startup_probe.md")
    args = parser.parse_args()

    if args.worker:
        _worker(args.family, args.pad_to)
        return

    payload = {
        "import_arbplusjax_api": _run_import_probe("from arbplusjax import api"),
        f"{args.family}_point_cached_path": _run_worker(args.family, args.pad_to),
    }

    out_json = REPO_ROOT / args.out_json
    out_md = REPO_ROOT / args.out_md
    out_json.parent.mkdir(parents=True, exist_ok=True)
    out_md.parent.mkdir(parents=True, exist_ok=True)
    out_json.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")
    out_md.write_text(
        "\n".join(
            [
                "Last updated: 2026-03-26T00:00:00Z",
                "",
                "# Matrix Point Startup Probe",
                "",
                "Generated by `benchmarks/matrix_point_startup_probe.py`.",
                "",
                f"- api import s: `{payload['import_arbplusjax_api']['seconds']}`",
                f"- family: `{payload[f'{args.family}_point_cached_path']['family']}`",
                f"- pad_to: `{payload[f'{args.family}_point_cached_path']['pad_to']}`",
                f"- prepare first s: `{payload[f'{args.family}_point_cached_path']['prepare_first_s']}`",
                f"- compile plus first cached apply s: `{payload[f'{args.family}_point_cached_path']['compile_plus_first_cached_apply_s']}`",
                f"- steady cached apply s: `{payload[f'{args.family}_point_cached_path']['steady_cached_apply_s']}`",
                "",
            ]
        )
        + "\n",
        encoding="utf-8",
    )
    print(f"Wrote: {out_json}")
    print(f"Wrote: {out_md}")


if __name__ == "__main__":
    main()
