from __future__ import annotations

import argparse
import json
import os
from pathlib import Path
import subprocess
import sys
import time


REPO_ROOT = Path(__file__).resolve().parents[1]


def _base_env() -> dict[str, str]:
    env = os.environ.copy()
    env["PYTHONPATH"] = str(REPO_ROOT / "src") + os.pathsep + env.get("PYTHONPATH", "")
    env.setdefault("JAX_PLATFORMS", "cpu")
    return env


def _run_import_probe(module_expr: str) -> dict[str, float]:
    code = (
        "import time\n"
        "t0=time.perf_counter()\n"
        f"{module_expr}\n"
        "print(time.perf_counter()-t0)\n"
    )
    completed = subprocess.run(
        [sys.executable, "-c", code],
        cwd=REPO_ROOT,
        env=_base_env(),
        capture_output=True,
        text=True,
        check=True,
    )
    seconds = float(completed.stdout.strip().splitlines()[-1])
    return {"seconds": round(seconds, 6)}


def _run_worker(family: str, pad_to: int) -> dict[str, float]:
    env = _base_env()
    completed = subprocess.run(
        [sys.executable, str(Path(__file__).resolve()), "--worker", "--family", family, "--pad-to", str(pad_to)],
        cwd=REPO_ROOT,
        env=env,
        capture_output=True,
        text=True,
        check=True,
    )
    return json.loads(completed.stdout)


def _worker(family: str, pad_to: int) -> None:
    import jax
    import jax.numpy as jnp

    from arbplusjax import api

    if family != "arb_hypgeom_1f1":
        raise ValueError(f"unsupported family {family!r}")

    a = jnp.asarray([1.0, 1.5], dtype=jnp.float64)
    b = jnp.asarray([2.0, 2.5], dtype=jnp.float64)
    z = jnp.asarray([0.25, 0.5], dtype=jnp.float64)

    t0 = time.perf_counter()
    _ = jax.devices()
    t1 = time.perf_counter()

    bound = api.bind_point_batch_jit(f"hypgeom.{family}", dtype="float64", pad_to=pad_to)
    t2 = time.perf_counter()
    out = bound(a, b, z)
    jax.block_until_ready(out)
    t3 = time.perf_counter()

    t4 = time.perf_counter()
    out = bound(a, b, z)
    jax.block_until_ready(out)
    t5 = time.perf_counter()

    payload = {
        "family": family,
        "pad_to": pad_to,
        "backend_init_s": round(t1 - t0, 6),
        "compile_plus_first_point_batch_s": round(t3 - t2, 6),
        "steady_point_batch_s": round(t5 - t4, 6),
    }
    print(json.dumps(payload, indent=2))


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--worker", action="store_true")
    parser.add_argument("--family", choices=("arb_hypgeom_1f1",), default="arb_hypgeom_1f1")
    parser.add_argument("--pad-to", type=int, default=4)
    parser.add_argument(
        "--out-json",
        default="benchmarks/results/hypgeom_point_startup_probe/hypgeom_point_startup_probe.json",
    )
    parser.add_argument(
        "--out-md",
        default="benchmarks/results/hypgeom_point_startup_probe/hypgeom_point_startup_probe.md",
    )
    args = parser.parse_args()

    if args.worker:
        _worker(args.family, args.pad_to)
        return

    key = f"{args.family}_point_path"
    payload = {
        "import_arbplusjax_api": _run_import_probe("from arbplusjax import api"),
        key: _run_worker(args.family, args.pad_to),
    }

    out_json = REPO_ROOT / args.out_json
    out_md = REPO_ROOT / args.out_md
    out_json.parent.mkdir(parents=True, exist_ok=True)
    out_md.parent.mkdir(parents=True, exist_ok=True)
    out_json.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")
    out_md.write_text(
        "\n".join(
            [
                "Last updated: 2026-03-26T00:00:00Z",
                "",
                "# Hypergeom Point Startup Probe",
                "",
                "Generated by `benchmarks/hypgeom_point_startup_probe.py`.",
                "",
                f"- api import s: `{payload['import_arbplusjax_api']['seconds']}`",
                f"- family: `{payload[key]['family']}`",
                f"- pad_to: `{payload[key]['pad_to']}`",
                f"- backend init s: `{payload[key]['backend_init_s']}`",
                f"- compile plus first point batch s: `{payload[key]['compile_plus_first_point_batch_s']}`",
                f"- steady point batch s: `{payload[key]['steady_point_batch_s']}`",
                "",
            ]
        )
        + "\n",
        encoding="utf-8",
    )
    print(f"Wrote: {out_json}")
    print(f"Wrote: {out_md}")


if __name__ == "__main__":
    main()
